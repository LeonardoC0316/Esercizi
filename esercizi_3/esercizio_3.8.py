
# list creation
locations = ["Rome", "London", "Berlin", "Tokyo", "Parigi"]
print(locations)

# print the list in alphabetic order
print(sorted(locations))

# dimostriamo che la lista non è stata modificata dalla funzione sorted

print(locations)

# invertiamo l'ordine della lista 
locations.reverse()

print(locations)

# la facciamo tornare come prima 

locations.reverse()
print(locations)

locations.sort()
print(locations)

print(locations.sort())



